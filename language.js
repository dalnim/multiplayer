// Object to store image URLs for each language
const imageUrls = {
  en: {
    shuffleThumbnail: "assets/images/home/auto-playlist-mobail.webp",
    shuffleThumbnailResponsive: "assets/images/home/auto-playlist-mobail.webp",
    image2: "path-to-english-image2.jpg"
  },
  ko: {
    shuffleThumbnail: "assets/images/home/auto-playlist-mobail-korean.webp",
    shuffleThumbnailResponsive: "assets/images/home/auto-playlist-mobail-korean.webp",
    image2: "path-to-korean-image2.jpg"
  }
};

 
// function for content update based on selected language
function updateLanguage(language) {
  if (language == 'en') {
    for (let key in jsonEnglish) {
      document.querySelector('.' + key).textContent = jsonEnglish[key];
    }
  } else if (language == 'ko') {
    for (let key in jsonKorean) {
      document.querySelector('.' + key).textContent = jsonKorean[key];
    }
  }
  // Update multiple image sources
  for (let imageId in imageUrls[language]) {
    document.querySelector('#' + imageId).src = imageUrls[language][imageId];
    // Save each image URL to localStorage
    localStorage.setItem(imageId + 'Src', imageUrls[language][imageId]);
  }
}

// set language on change and update the localization
document.querySelector("#language").addEventListener('change', function () {
  const selectedLanguage = this.value;
  localStorage.setItem('selectedLanguage', selectedLanguage);
  updateLanguage(selectedLanguage);
  console.log('selected lang', selectedLanguage);
})
// on page load, set the selected language from local storage
document.addEventListener('DOMContentLoaded', function () {
  const storedLanguage = localStorage.getItem('selectedLanguage') || 'en';
  document.querySelector("#language").value = storedLanguage;
  updateLanguage(storedLanguage);
  console.log('stored lang', storedLanguage);

  // Load image sources from localStorage
  for (let imageId in imageUrls[storedLanguage]) {
    const storedImageSrc = localStorage.getItem(imageId + 'Src') || imageUrls[storedLanguage][imageId];
    document.querySelector('#' + imageId).src = storedImageSrc;
  }
})
